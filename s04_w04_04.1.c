#include <stdio.h>

int main (){            // กำหนดตัวเเปร int เเละให้ค่า 90.5 (ซึ่งผิด เพราะ int เก็บเฉพาะจำนวนเต็ม)
    int score = 90.5;   // เเสดงผลลัพธ์ (ค่าทศนิยมจะถูกตัดออก เหลือเเค่ 90)

    //คำถามที่ 1: ผลลัพธ์คือ 90 เพราะ int จะตัดทศนิยมออก
    //คำถามที่ 2: เลือก int ผิด เพราะเราต้องการเก็บค่าทศนิยม
    //คำถามที่ 3: สรุปผล - ถ้าใช้ชนิดข้อมูลไม่เหมาะสม เช่น int กับค่าทศนิยม จะทำให้ข้อมูลไม่ตรงตามที่ต้องการ ส่งผลต่อความถูกต้องของโปรเเกรม
    printf("Score = %d\n", score);
    return 0;
}