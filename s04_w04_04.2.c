#include <stdio.h>

int main(){     // กำหนดตัวเเปรชนิด float (สามารถเก็บค่าทศนิยม)
    float score = 90.5;

    //คำถามที่ 1: ผลลัพธ์คือ 90.5 เเสดงค่าถูกต้อง
    //คำถามที่ 2: เลือก float ถูกต้อง เพราะสามารถเก็บค่าทศนิยมได้
    //คำถามที่ 3:สรุปผล - ถ้าเลือกชนิดข้อมูลเหมาะสม โปรเเกรมจะทำงานถูกต้องเเละเเสดงผลลัพธ์ที่ตรงกับความต้องการ
    printf("Score = %.1f\n", score);
    return 0;
}