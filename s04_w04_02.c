#include <stdio.h> //เเก้: ต้องมี # เเละ < >

int main() {       //int: Keyboard, main: Identifer
    int a = 10;    //เเก้ ; เพิ่ม ; ปิดท้าย/ int: Keyword, a: Identifier, 10:Constan
    printf("a = %d", a); //เเก้: เพิ่ม ; ปิดท้าย / printf: Identifier, "a = %d": Cons
    return 0;  //เเก้: เพิ่ม ; ปิดท้าย / return: Keyword, 0: Constant
}